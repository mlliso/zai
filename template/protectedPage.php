    <div>Autor: Michał Lisiecki</div>
    <div>Treść zadania:</div>
    <div class="long_text">Stwórz własna witrynę, w której dostęp do pewnych podstron będzie wymagał zalogowania. Należy 
        zaimplementować własny mechanizm uwierzytelniania wliczając w to tworzenie konta nowego
        użytkownika, logowanie, wylogowanie oraz usuwanie użytkowników. Formularz do tworzenia konta
        nowego użytkownika powinien zawierać miedzy innymi pola: nazwa użytkownika, hasło,
        potwierdzenie hasła, adres e-mail. Sprawdzenie poprawności wprowadzonych danych (np. czy zgadza
        się hasło w obydwu polach, prosta walidacja adresu e-mail) powinno się odbyć po stronie klienta.
        System uwierzytelniania powinien być wkomponowany w wygląd strony. Należy wykorzystać
        technologie PHP 5 i MySQL 5 oraz JavaScript. Mechanizm uwierzytelniania ma byd stworzony
        samodzielnie, bez korzystania z gotowych frameworków - można bez żadnych ograniczeń
        wykorzystywać materiały do kursu. Stronę należy umieścić  na dowolnym serwerze oraz adres
        dostępu do strony umieścić w sprawozdaniu opisującym sposób konfiguracji serwera i uruchomienia
        witryny.</div>
